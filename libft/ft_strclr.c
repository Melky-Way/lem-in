#include "libft.h"

void	ft_strclr(char *s)
{
	size_t	i;

	i = 0;
	if (s != NULL)
	{
		while (s[i])
			i++;
		while (i)
		{
			s[i] = '\0';
			i--;
		}
		s[i] = '\0';
	}
}
